let a = '2';
let b = 0;

switch(+a){
    case b:
        console.log("Case 0");
        break
    case b+1:
        console.log("Case 1");
        break
    case b+2:
        console.log("Case 2");
        break
    case b+3:
        console.log("Case 3");
        break
    default:
        console.log("Default Case");
}


